void uart1_send(char *msg,unsigned char cnt)
{
  unsigned char i = 0;
  unsigned char ch = 0;
  for (i = 0; i < cnt; i++) {
    ch = *msg;
		while((USART1->SR&0X40)==0);//循环发送,直到发送完毕   
		USART1->DR = (u8) ch; 
	msg++;
  }
}
 

